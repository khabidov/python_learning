class Math:
    def __init__(self, a, b):
        self.a=a
        self.b=b

    def addition(self):
        return self.a+self.b

    def multiplication(self):
        return self.a*self.b

    def division(self):
        return  self.a/self.b

    def subtruction(self):
        return self.a-self.b


